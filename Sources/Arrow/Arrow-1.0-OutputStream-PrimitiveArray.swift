public extension OutputStreamProtocol {
    /// Return the stored, untyped pointer as a typed pointer to the `GArrowOutputStream` instance.
    @inlinable var output_stream_ptr: UnsafeMutablePointer<GArrowOutputStream>! { return ptr?.assumingMemoryBound(to: GArrowOutputStream.self) }

    @inlinable func align(alignment: gint32) throws -> Bool {
        var error: UnsafeMutablePointer<GError>?
        let rv = ((garrow_output_stream_align(output_stream_ptr, alignment, &error)) != 0)
        if let error = error { throw GLibError(error) }
        return rv
    }

    @inlinable func write<RecordBatchT: RecordBatchProtocol>(recordBatch: RecordBatchT, options: WriteOptionsRef? = nil) throws -> gint64 {
        var error: UnsafeMutablePointer<GError>?
        let rv = garrow_output_stream_write_record_batch(output_stream_ptr, recordBatch.record_batch_ptr, options?.write_options_ptr, &error)
        if let error = error { throw GLibError(error) }
        return rv
    }
    @inlinable func write<RecordBatchT: RecordBatchProtocol, WriteOptionsT: WriteOptionsProtocol>(recordBatch: RecordBatchT, options: WriteOptionsT?) throws -> gint64 {
        var error: UnsafeMutablePointer<GError>?
        let rv = garrow_output_stream_write_record_batch(output_stream_ptr, recordBatch.record_batch_ptr, options?.write_options_ptr, &error)
        if let error = error { throw GLibError(error) }
        return rv
    }

    @inlinable func write<TensorT: TensorProtocol>(tensor: TensorT) throws -> gint64 {
        var error: UnsafeMutablePointer<GError>?
        let rv = garrow_output_stream_write_tensor(output_stream_ptr, tensor.tensor_ptr, &error)
        if let error = error { throw GLibError(error) }
        return rv
    }

    @inlinable var parentInstance: GObject {
        get {
            let rv = output_stream_ptr.pointee.parent_instance
            return rv
        }
    }

}



